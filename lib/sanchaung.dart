import 'package:flutter/material.dart';
import 'package:get/get.dart';

void main() {
  runApp(MyApp());
}

class MyApp extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: '',
      theme: ThemeData(
        primarySwatch: Colors.blue,
      ),
      home:sanchaung(),
    );
  }
}

class sanchaung extends StatefulWidget {
  @override
  _sanchaungState createState() => _sanchaungState();
}

class _sanchaungState extends State<sanchaung> {
  final List<Sanres> cusines = [
       
       Sanres(
      name: 'Pork Queen Restaurant',
      image: AssetImage("images/pork.jpg"),
      address:'Add:11111,Shin Sawpu Pagoda St,Sanchaung,Yangon',
      phone:'Tel:+959 754 628 018',
      ),
    
       Sanres(
      name: 'Sopyae Shan Noodle Restaurant',
      image: AssetImage("images/shan7.jpg"),
      address:'Add:No. 26, Shan Kone Street ,SanChaung , Yangon',
      phone:'Tel:+959 772 523139',
      ),
      Sanres(
      name: 'Xi Yang Yang',
      image: AssetImage("images/xi1.jpg"),
      address:'Add:No.14,Nyaung Tong Rd,SanChaung Township,Yangon',
      phone:'Tel:+951 502 582',
     
      
    ),
  ];
 String searchText = '';
 
  @override
  Widget build(BuildContext context) {
    final filteredcusines = searchText.isEmpty
        ? cusines
        : cusines.where((cusines) => cusines.name.toLowerCase().contains(searchText.toLowerCase())).toList();
    return Scaffold(
      appBar: AppBar(
        backgroundColor:Colors.blueGrey,
        title: Text('Restaurant Directory',style: TextStyle(fontSize: 20,      
        color:Colors.white),),
      ),
      body: Column(
        children: [
          Padding(
            padding: const EdgeInsets.all(8.0),
            child: TextField(
              onChanged: (value) {
                setState(() {
                  searchText = value;
                });
              },
              decoration: InputDecoration(
                labelText: 'Search by restaurant name',
                prefixIcon: Icon(Icons.search,color: Colors.blueGrey, size: 24.0),
                 border:OutlineInputBorder(                 
                 borderRadius: BorderRadius.circular(10),                             
             
                ),                
                ),
              ),
          ),
          Expanded(
            child: ListView.builder(
              itemCount: filteredcusines.length,
              itemBuilder: (context, index) {
                final item = filteredcusines[index];
                return ListTile(
                  leading: CircleAvatar(
                    backgroundImage: item.image,
                  ),
                  title: Text(item.name,style:(TextStyle(color:Colors.blueGrey,fontWeight: FontWeight.bold))),
                 
                  onTap: () {
                    Navigator.push(
                      context,
                      MaterialPageRoute(
                        builder: (context) => ItemDetailsPage(item: item),
                      ),
                    );
                  },
                );
              },
            ),
          ),
        ],
      ),
    );
  }
}

